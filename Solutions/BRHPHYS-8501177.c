#include<stdio.h>
int main()
{
  int i,j,k;
  int t,a,b,pi,si;
  float av=0;
  scanf("%d",&t);
  a=0;b=0;
  while(t--)
  {
   scanf("%d%d",&pi,&si);
   a+=pi;
   b+=si;
  }
  av=0;
  av=(float)((b*100)/a);
  if(av>=90&&av<=100)
    printf("A\n");
  else if(av>=80&&av<=89)
   printf("B\n");
  else if(av>=70&&av<=79)
   printf("C\n");
  else if(av>=60&&av<=69)
   printf("D\n");
  else
   printf("F\n");
return 0;
}


